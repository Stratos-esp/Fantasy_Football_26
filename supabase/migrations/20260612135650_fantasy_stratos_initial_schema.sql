);

create table public.fantasy_lineups (
  id uuid primary key default gen_random_uuid(),
  matchday_id uuid not null references public.fantasy_matchdays(id) on delete cascade,
  league_member_id uuid not null references public.fantasy_league_members(id) on delete cascade,
  formation text not null default '4-4-2',
  captain_player_id uuid references public.fantasy_players(id),
  submitted_at timestamptz,
  total_points numeric(10,2) not null default 0,
