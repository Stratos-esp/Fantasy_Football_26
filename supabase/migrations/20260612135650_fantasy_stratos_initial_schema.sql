create function private.fantasy_set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger fantasy_users_fantasy_set_updated_at before update on public.fantasy_users for each row execute function private.fantasy_set_updated_at();
create trigger fantasy_bids_fantasy_set_updated_at before update on public.fantasy_bids for each row execute function private.fantasy_set_updated_at();
create trigger fantasy_leagues_fantasy_set_updated_at before update on public.fantasy_leagues for each row execute function private.fantasy_set_updated_at();

do $$
declare table_name text;
begin
  foreach table_name in array array[
    'fantasy_users','fantasy_sessions','fantasy_leagues','fantasy_league_members','fantasy_teams','fantasy_players','fantasy_player_values',
    'fantasy_squads','fantasy_matchdays','fantasy_lineups','fantasy_lineup_players','fantasy_market_listings','fantasy_bids','fantasy_transfers',
    'fantasy_fixtures','fantasy_player_match_stats','fantasy_player_scores','fantasy_chat_messages','fantasy_notifications','fantasy_audit_log'
  ] loop
    execute format('alter table public.%I enable row level security', table_name);
    execute format('revoke all on table public.%I from anon, authenticated', table_name);
    execute format('grant select, insert, update, delete on table public.%I to service_role', table_name);
  end loop;
end $$;

grant usage on schema public to service_role;
grant usage on schema private to service_role;
grant usage, select on all sequences in schema public to service_role;
revoke all on function private.fantasy_set_updated_at() from public, anon, authenticated;
grant execute on function private.fantasy_set_updated_at() to service_role;
