create schema if not exists private;

create type public.fantasy_league_role as enum ('owner', 'admin', 'member');
create type public.fantasy_player_position as enum ('POR', 'DEF', 'MED', 'DEL');
create type public.fantasy_market_kind as enum ('bid', 'fixed', 'clause', 'direct');
create type public.fantasy_market_status as enum ('open', 'accepted', 'expired', 'cancelled');

create table public.fantasy_users (
  id uuid primary key default gen_random_uuid(),
  username text not null unique check (username ~ '^[a-z0-9_.-]{3,24}$'),
  email text,
  password_hash text not null,
  display_name text not null,
  avatar_url text,
  is_active boolean not null default true,
  last_login_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create unique index fantasy_users_email_unique on public.fantasy_users (lower(email)) where email is not null;

create table public.fantasy_sessions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.fantasy_users(id) on delete cascade,
  token_hash text not null unique,
  expires_at timestamptz not null,
  created_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  user_agent text,
  ip_address inet
);
create index fantasy_sessions_user_id_idx on public.fantasy_sessions(user_id);
create index fantasy_sessions_expires_at_idx on public.fantasy_sessions(expires_at);

create table public.fantasy_leagues (
  id uuid primary key default gen_random_uuid(),
  owner_id uuid not null references public.fantasy_users(id),
  name text not null,
  slug text not null unique,
  invite_code text not null unique,
  competition_provider text not null default 'api-football',
  competition_external_id integer not null default 140,
  season integer not null,
  starting_budget numeric(12,2) not null default 100000000,
  squad_size integer not null default 15 check (squad_size between 11 and 30),
  is_private boolean not null default true,
  settings jsonb not null default '{"captain":true,"captainMultiplier":1.5,"bench":true,"benchSlots":4,"market":{"fantasy_bids":true,"fixedPrice":true,"clauses":true,"directTransfers":true}}'::jsonb,
  scoring_rules jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.fantasy_league_members (
  id uuid primary key default gen_random_uuid(),
  league_id uuid not null references public.fantasy_leagues(id) on delete cascade,
  user_id uuid not null references public.fantasy_users(id) on delete cascade,
  role public.fantasy_league_role not null default 'member',
  team_name text not null,
  budget numeric(12,2) not null,
  total_points numeric(10,2) not null default 0,
  joined_at timestamptz not null default now(),
  unique (league_id, user_id),
  unique (league_id, team_name)
);
create index fantasy_league_members_league_points_idx on public.fantasy_league_members(league_id, total_points desc);

create table public.fantasy_teams (
  id uuid primary key default gen_random_uuid(),
  external_id integer not null,
  competition_external_id integer not null,
  season integer not null,
  name text not null,
  short_name text,
  badge_url text,
  colors jsonb not null default '[]'::jsonb,
  unique (external_id, season)
);

create table public.fantasy_players (
  id uuid primary key default gen_random_uuid(),
  external_id integer not null,
  team_id uuid references public.fantasy_teams(id),
  season integer not null,
  name text not null,
  common_name text,
  position public.fantasy_player_position not null,
  photo_url text,
  status text not null default 'available',
  metadata jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now(),
  unique (external_id, season)
);
create index fantasy_players_team_position_idx on public.fantasy_players(team_id, position);

create table public.fantasy_player_values (
  id bigint generated by default as identity primary key,
  player_id uuid not null references public.fantasy_players(id) on delete cascade,
  value numeric(12,2) not null check (value >= 0),
  recorded_at timestamptz not null default now()
);
create index fantasy_player_values_latest_idx on public.fantasy_player_values(player_id, recorded_at desc);

create table public.fantasy_squads (
  id uuid primary key default gen_random_uuid(),
  league_member_id uuid not null references public.fantasy_league_members(id) on delete cascade,
  player_id uuid not null references public.fantasy_players(id),
  purchase_price numeric(12,2) not null default 0,
  clause_value numeric(12,2),
  acquired_at timestamptz not null default now(),
  unique (league_member_id, player_id)
);
create index fantasy_squads_player_idx on public.fantasy_squads(player_id);

create table public.fantasy_matchdays (
  id uuid primary key default gen_random_uuid(),
  league_id uuid not null references public.fantasy_leagues(id) on delete cascade,
  number integer not null,
  starts_at timestamptz,
  locks_at timestamptz,
